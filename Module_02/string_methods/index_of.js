// indexOf(): Takes takes a substring and if the substring exists in a string it returns the first position of the substring if does not exist it returns -1

string.indexOf(substring)
let string = 'JavaScript-Forever'
console.log(string.indexOf('S'))          // S
console.log(string.indexOf('v'))       // 3
console.log(string.indexOf(''))       // -1
console.log(string.indexOf('c'))          // 6
console.log(string.indexOf('JavaScript')) // 9

